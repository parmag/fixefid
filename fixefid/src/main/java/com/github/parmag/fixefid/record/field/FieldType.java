package com.github.parmag.fixefid.record.field;

/**
 * The enum that represents the field type. A field can be alphanumeric or numeric
 * 
 * @author Giancarlo Parma
 *
 * @since 1.0
 */
public enum FieldType {
	/**
	 * the alphanumeric type
	 */
	AN, 
	/**
	 * the numeric type
	 */
	N,
	/**
	 * the composite type
	 */
	CMP 
}
