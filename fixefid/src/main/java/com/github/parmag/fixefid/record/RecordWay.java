package com.github.parmag.fixefid.record;

/**
 * The enum that represents the record way
 * 
 * @author Giancarlo Parma
 * 
 * @since 1.0
 *
 */
public enum RecordWay {
	/**
	 * The record way IN, that's the record is read from a source (typically a text file)
	 */
	IN, 
	/**
	 * The record way OUT, that's the record is write to a source (typically a text file)
	 */
	OUT
}
